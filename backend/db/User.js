const mongoose = require('mongoose');	

const testSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    }
})

const Test = new mongoose.model("users", testSchema)
module.exports = Test;